package com.example.level3;

public class text {
    private String big;
    private String guo;
    private String red;
    public text(){

    }

    public text(String big,String guo,String red){
        this.big=big;
        this.guo=guo;
        this.red=red;
    }

    public String getBig() {
        return big;
    }

    public void setBig(String Big) {
        this.big = Big;
    }

    public String getGuo() {
        return guo;
    }

    public void setGuo(String Guo) {
        this.guo = Guo;
    }
    public String getRed() {
        return red;
    }

    public void setRed(String Red) {
        this.red = Red;
    }
}
